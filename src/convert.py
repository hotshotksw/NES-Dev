#!/usr/bin/env python3


def rotate_byte_array(byte_data_text):
    """
    Rotates a byte array so that:
    - First row contains the last values from each original row
    - Second row contains the second-to-last values from each original row
    - And so on...
    """

    # Parse the input text to extract hex values
    lines = byte_data_text.strip().split("\n")
    byte_rows = []

    for line in lines:
        # Remove .byte directive and split by commas
        if ".byte" in line:
            # Extract everything after '.byte '
            hex_part = line.split(".byte ")[1].strip()
            # Split by commas and clean up each hex value
            hex_values = [val.strip().replace("$", "") for val in hex_part.split(",")]
            byte_rows.append(hex_values)

    # Determine dimensions
    num_rows = len(byte_rows)
    num_cols = len(byte_rows[0]) if byte_rows else 0

    print(f"Original array: {num_rows} rows Ã— {num_cols} columns")

    # Create rotated array (16 rows as requested)
    rotated_rows = []

    # For each column position (from right to left, starting at column 15)
    for col in range(min(16, num_cols) - 1, -1, -1):
        new_row = []
        # Collect values from that column across all rows
        for row in range(num_rows):
            if col < len(byte_rows[row]):
                new_row.append(byte_rows[row][col])
        rotated_rows.append(new_row)

    # Output the rotated array
    print("\nRotated array:")
    for i, row in enumerate(rotated_rows):
        # Format as assembly .byte directive
        hex_values = [f"${val}" for val in row]
        line = "  .byte " + ",".join(hex_values)
        print(line)

    return rotated_rows


